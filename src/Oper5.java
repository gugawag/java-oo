/*
 * Operadores aritméticos de incrementação de decrementação
 */

public class Oper5 {
	public static void main(String args[]) {

		System.out.println("Teste dos operadores ++ e --");
		int i = 3;
		System.out.println("Antes de i++, i = " + i);
		i++;
		System.out.println("Depois de i++, i = " + i);
		i = 3;
		System.out.println("Antes de i--, i = " + i);
		i--;
		System.out.println("Depois de i--, i = " + i);
	} // main
} // Oper5
